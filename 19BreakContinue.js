// break keyword

for(let i = 1; i<=10; i++){
    if(i===4){
        break;
    }
    console.log(i);
}
// break keyword agar true hoga toh wo loop ke baahar chl jaayega isliye ye loop mein number 3 h tk print hoga

// continue keyword

for(let j = 1; j<=10; j++){
    if(j===4){
        continue;
    }
    console.log(j);
}
// continue keyword agar true hoga toh wo firse loop mein chl jaayega 
//isse iss loop mein 4 number print nai hoga    